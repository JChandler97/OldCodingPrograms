// This program is to find out how far a car travelled while going 60 MPH for 5 hours.


public class Traffic {
	public static void main (String[] args){
		int speed = 60;
		int time = 5;
		int distance = speed * time;
		System.out.println("The car is traveling " + distance + " miles.");
	}
}